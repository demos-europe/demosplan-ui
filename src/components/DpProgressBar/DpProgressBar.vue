<template>
  <div
    :class="{ 'c-progress-bar--no-border': hideBorder }"
    class="c-progress-bar">
    <template v-if="indeterminate">
      <div class="u-mb-0_25 weight--bold">
        {{ label }}
      </div>

      <div class="c-progress-bar__line is-animated" />
    </template>

    <template v-else>
      <div class="u-mb-0_25 weight--bold">
        {{ label }}
      </div>

      <div class="bg-color--grey-light-2">
        <div
          :class="{ 'c-progress-bar__line--blue': alternativeColor }"
          :style="style"
          class="u-mb-0_25 c-progress-bar__line" />
      </div>
      <span v-if="!hidePercentage">
        {{ percentage }}%
      </span>
    </template>
  </div>
</template>

<script>
export default {
  name: 'DpProgressBar',

  props: {
    alternativeColor: {
      type: Boolean,
      required: false,
      default: false
    },
    hideBorder: {
      type: Boolean,
      required: false,
      default: false
    },
    hidePercentage: {
      type: Boolean,
      required: false,
      default: false
    },

    indeterminate: {
      type: Boolean,
      required: false,
      default: false
    },

    label: {
      type: String,
      required: false,
      default: ''
    },

    percentage: {
      type: Number,
      required: false,
      default: 0
    }
  },

  computed: {
    style () {
      return 'width: ' + this.percentage + '%;'
    }
  }
}
</script>
